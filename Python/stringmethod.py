name = "Garima kushwaha"

#convert to uppercase
print(name.upper())

#convert to lowercase
print(name.lower())

#to find index of letter or word
print(name.find('a'))
print(name.find("kush"))
print(name.find("kuSh"))

#to replace name
print(name.replace("Garima","Mala"))

#to search a letter not change
print('K' in name)
print('k' in name)